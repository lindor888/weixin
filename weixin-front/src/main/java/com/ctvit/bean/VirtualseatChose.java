package com.ctvit.bean;

public class VirtualseatChose {
	private String choseID;
	private String openID;
	private String virtualSeatID;
	private String nickname;
	private String headimgurl;
	private int lineNum;
	private int columnNum;

	public String getChoseID() {
		return this.choseID;
	}

	public void setChoseID(String choseID) {
		this.choseID = choseID;
	}

	public String getOpenID() {
		return this.openID;
	}

	public void setOpenID(String openID) {
		this.openID = openID;
	}

	public String getVirtualSeatID() {
		return this.virtualSeatID;
	}

	public void setVirtualSeatID(String virtualSeatID) {
		this.virtualSeatID = virtualSeatID;
	}

	public String getNickname() {
		return this.nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getHeadimgurl() {
		return this.headimgurl;
	}

	public void setHeadimgurl(String headimgurl) {
		this.headimgurl = headimgurl;
	}

	public int getLineNum() {
		return this.lineNum;
	}

	public void setLineNum(int lineNum) {
		this.lineNum = lineNum;
	}

	public int getColumnNum() {
		return this.columnNum;
	}

	public void setColumnNum(int columnNum) {
		this.columnNum = columnNum;
	}
}