package com.ctvit.bean;

public class Mapkeyword {
	
	private String mapKeywordID;
	private String mapKeywordName;
	private String createuser;
	private String createtime;
	private int mapstatus;
	public String getMapKeywordID() {
		return mapKeywordID;
	}
	public void setMapKeywordID(String mapKeywordID) {
		this.mapKeywordID = mapKeywordID;
	}
	public String getMapKeywordName() {
		return mapKeywordName;
	}
	public void setMapKeywordName(String mapKeywordName) {
		this.mapKeywordName = mapKeywordName;
	}
	public String getCreateuser() {
		return createuser;
	}
	public void setCreateuser(String createuser) {
		this.createuser = createuser;
	}
	public String getCreatetime() {
		return createtime;
	}
	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}
	public int getMapstatus() {
		return mapstatus;
	}
	public void setMapstatus(int mapstatus) {
		this.mapstatus = mapstatus;
	}
	
	
}
