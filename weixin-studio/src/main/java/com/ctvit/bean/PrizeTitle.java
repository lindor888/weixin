package com.ctvit.bean;

public class PrizeTitle {

	private String prizetitleId;
	private String prizetitleName;
	private String createtime;
	private String starTime;
	private String endTime;
	private String waccountId;
	private int flag;

	public String getPrizetitleId() {
		return prizetitleId;
	}

	public void setPrizetitleId(String prizetitleId) {
		this.prizetitleId = prizetitleId;
	}

	public String getPrizetitleName() {
		return prizetitleName;
	}

	public void setPrizetitleName(String prizetitleName) {
		this.prizetitleName = prizetitleName;
	}

	public String getCreatetime() {
		return createtime;
	}

	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}

	public String getStarTime() {
		return starTime;
	}

	public void setStarTime(String starTime) {
		this.starTime = starTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getWaccountId() {
		return waccountId;
	}

	public void setWaccountId(String waccountId) {
		this.waccountId = waccountId;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

}
