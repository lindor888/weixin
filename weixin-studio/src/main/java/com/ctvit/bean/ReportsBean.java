package com.ctvit.bean;

public class ReportsBean {

	private String reportsID;
	private String qrcodeID;
	private String openID;
	private String reportsTime;
	private String nickName;
	private String headImg;
	private String city;
	private String waccount;

	public String getReportsID() {
		return reportsID;
	}

	public void setReportsID(String reportsID) {
		this.reportsID = reportsID;
	}

	public String getQrcodeID() {
		return qrcodeID;
	}

	public void setQrcodeID(String qrcodeID) {
		this.qrcodeID = qrcodeID;
	}

	public String getOpenID() {
		return openID;
	}

	public void setOpenID(String openID) {
		this.openID = openID;
	}

	public String getReportsTime() {
		return reportsTime;
	}

	public void setReportsTime(String reportsTime) {
		this.reportsTime = reportsTime;
	}

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public String getHeadImg() {
		return headImg;
	}

	public void setHeadImg(String headImg) {
		this.headImg = headImg;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getWaccount() {
		return waccount;
	}

	public void setWaccount(String waccount) {
		this.waccount = waccount;
	}

}
