package com.ctvit.bean;

public class MeetingContent {

	private int meetingcontentid;
	private String meetingid;
	private String content;
	private String contentserial;
	private String contenttime;
	private String contentpersion;
	private String contentgroup;

	public int getMeetingcontentid() {
		return meetingcontentid;
	}

	public void setMeetingcontentid(int meetingcontentid) {
		this.meetingcontentid = meetingcontentid;
	}

	public String getMeetingid() {
		return meetingid;
	}

	public void setMeetingid(String meetingid) {
		this.meetingid = meetingid;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getContentserial() {
		return contentserial;
	}

	public void setContentserial(String contentserial) {
		this.contentserial = contentserial;
	}

	public String getContenttime() {
		return contenttime;
	}

	public void setContenttime(String contenttime) {
		this.contenttime = contenttime;
	}

	public String getContentpersion() {
		return contentpersion;
	}

	public void setContentpersion(String contentpersion) {
		this.contentpersion = contentpersion;
	}

	public String getContentgroup() {
		return contentgroup;
	}

	public void setContentgroup(String contentgroup) {
		this.contentgroup = contentgroup;
	}

}
